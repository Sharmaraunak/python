def evalQuadratic(a,b,c,x):
    '''
    input:a,b,c numerical value of coeeficients
          x:the variable og quadratic equation
    return the all made quadtaric equation
    '''
    return a*(x**2)+b*x+c
evalQuadratic(4,3,2,1)
